const reverse = require('./reverse');

test('should be equal to ilasunuy',()=>{
    expect(reverse('yunusali')).toBe('ilasunuy');
})

test('should be equal to dluohs',()=>{
    expect(reverse('should')).toBe('dluohs');
})

test('should be equal to lauqe',()=>{
    expect(reverse('equal')).toBe('lauqe');
})

test('should be equal to eb',()=>{
    expect(reverse('be')).toBe('eb');
})

